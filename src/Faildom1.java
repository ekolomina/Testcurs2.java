public class Faildom1 {
    // Составить процедуру, "рисующую" на экране
    // горизонтальную линию из 10 символов "*"

    public static void main(String[] args) {
        print5 (10);

    }


    public static void print5 ( int n) {
        for (int i = 0; i < n; i++){
            System.out.print("*");

        }
    }
}
